    public partial class Page5 : Page
    {
        public Page5()
        {
            InitializeComponent();
        }
        private void TextBox_GotFocus(object sender, RoutedEventArgs e)
        {
            TextBox textBox = (TextBox)sender;
            if (textBox.Text == "Электронная почта")
            {
                textBox.Text = "";
                
            }
        }
        private void Move6page(object sender, RoutedEventArgs e)
        {
            this.NavigationService.Navigate(new Page6());
        }
        private void TextBox_LostFocus(object sender, RoutedEventArgs e)
        {
            TextBox textBox = (TextBox)sender;
            if (string.IsNullOrWhiteSpace(textBox.Text))
            {
                textBox.Text = "Электронная почта";
                textBox.Foreground = Brushes.Black; // Верните цвет текста обратно в серый
            }
        }
    }
